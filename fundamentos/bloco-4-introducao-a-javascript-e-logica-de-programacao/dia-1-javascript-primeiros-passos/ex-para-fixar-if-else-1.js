// Utilize if/else para fazer um programa que retorne o maior de dois números. Defina, no começo do programa, duas constantes com os valores que serão comparados.

const a = 10;
const b = 3;

if (a > b) {
    console.log('Constante a é maior que b');
} else if (a < b) {
    console.log('Constante b é maior que a')
} else {
    console.log('As duas constantes tem valores iguais')
}